#include <stdio.h>
#include <stdlib.h>
int main()
{
    //uppercase letters
    printf("Integer equivalents of uppercase letters:\n");
    printf("A:%d\n",'A');
    printf("B: %d\n", 'B');
    printf("C: %d\n", 'C');
    //Lowercase letters
    printf("Integer equivalents of lowercase letters:\n");
    printf("a: %d\n", 'a');
    printf("b: %d\n", 'b');
    printf("c: %d\n", 'c');
    //Digits
    printf("Integer equivalents of digits:\n");
    printf("0: %d\n", '0');
    printf("1: %d\n", '1');
    printf("2: %d\n", '2');
    //Special symbols
    printf("Integer equivalents of special symbols:\n");
    printf("$: %d\n", '$');
    printf("*: %d\n", '*');
    printf("+: %d\n", '+');
    printf("/: %d\n", '/');
    //Blank character
    printf("Integer equivalent of the blank characters:\n");
    return 0;
}

